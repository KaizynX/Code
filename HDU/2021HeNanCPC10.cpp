const int N = 1e5+7;

int n, k;
int a[N];

inline void solve() {
  scanf("%d%d", &n, &k);
  for (int i = 1; i <= n; ++i) scanf("%d", a+i);
  for (int i = 1; i <= n; ++i) {
    int cnt = 0;
    for (int j = i-1; j; --j) {
      cnt += a[j] > a[i];
      if (cnt == k) {
        printf("%d\n", a[j]);
        break;
      }
    }
    if (cnt < k) puts("-1");
  }
}


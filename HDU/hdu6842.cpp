constexpr int md = 998244353;
using mint = Modular<std::integral_constant<decay<decltype(md)>::type, md>>;

struct Combination {
  int fac[N<<1], inv[N<<1];
  void init(const int &n) {
    fac[0] = inv[0] = fac[1] = inv[1] = 1;
    for (int i = 2; i <= n; ++i) {
      fac[i] = 1ll*fac[i-1]*i%MOD;
      inv[i] = 1ll*(MOD-MOD/i)*inv[MOD%i]%MOD;
    }
    for (int i = 2; i <= n; ++i) {
      inv[i] = 1ll*inv[i]*inv[i-1]%MOD;
    }
  }
  int A(const int &p, const int &q) {
    return p >= q ? 1ll*fac[p]*inv[p-q]%MOD : 0;
  }
  int C(const int &p, const int &q) {
    return p >= q ? 1ll*fac[p]*inv[q]%MOD*inv[p-q]%MOD : 0;
  }
};

int n, m;
Combination comb;
mint p, q, p0, p1, p2, p3;
mint fac[N], sum[N], pw1[N], pw2[N], pw3[N];
// p0 n, m
// p1 n-1, m
// p2 n, m-1
// p3 n-1, m-1

inline void solve() {
  cin >> n >> m >> p >> q;
  p /= 100; q /= 100;
  p0 = mint(1)/(-(-p+1)*(-q+1)+1);
  p1 = (-p+1)*q*p0;
  p2 = p*(-q+1)*p0;
  p3 = p*q*p0;
  // put i's to m-1
  pw1[0] = 1;
  sum[0] = 1;
  for (int i = 1; i < n; ++i) {
    pw1[i] = pw1[i-1]*p1;
    sum[i] = pw1[i]*fac[m+i-1]/fac[m-1]/fac[i];
    sum[i] += sum[i-1];
  }
  pw2[0] = pw3[0] = 1;
  for (int i = 1; i < m; ++i) {
    pw2[i] = pw2[i-1]*p2;
    pw3[i] = pw3[i-1]*p3;
  }
  mint res = 0;
  for (int t1, t2, t3 = 0; t3 < m && t3 < n; ++t3) {
    // t2+t3 == m-1
    // t1+t3 < n
    t2 = m-1-t3;
    t1 = n-1-t3;
    res += sum[t1]*pw2[t2]*pw3[t3]*comb.C(m-1, t2);
  }
  // res = sum [n=i, m=1]
  cout << res*p0*p << endl;
}

signed main() {
  ios::sync_with_stdio(false); cin.tie(NULL); cout.tie(NULL);
  fac[0] = 1;
  for (int i = 1; i < N; ++i) fac[i] = fac[i-1]*i;
  comb.init(200000);
  int T = 1;
  cin >> T;
  for (int t = 1; t <= T; ++t) {
    solve();
  }
  return 0;
}
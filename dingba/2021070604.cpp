constexpr int md = 1e9+7;
using Mint = Modular<std::integral_constant<decay<decltype(md)>::type, md>>;

const int N = 15;
const int M = 1e3+7;

int n, m;
string s;
Mint dp[2][1<<N], res[N+1];

int code(vector<int> &y) {
  int x = 0;
  for (int i = 0; i < n; ++i) {
    x |= (y[i+1]-y[i])<<i;
  }
  return x;
}

vector<int> decode(int x) {
  vector<int> y(n+1, 0);
  for (int i = 0; i < n; ++i) {
    y[i+1] = y[i]+((x>>i)&1);
  }
  return y;
}

inline void solve() {
  cin >> s >> m;
  n = s.size();
  memset(res, 0, sizeof(Mint)*(n+1));
  memset(dp[0], 0, sizeof(Mint)*(1<<n));
  dp[0][0] = 1;
  vector<int> y, z(n+1, 0);
  for (int i = 1; i <= m; ++i) {
    memset(dp[i&1], 0, sizeof(Mint)*(1<<n));
    Mint *cur = dp[i&1], *pre = dp[~i&1];
    for (int j = 0; j < 1<<n; ++j) if (pre[j]) {
      y = decode(j);
      for (char ch : {'A', 'C', 'G', 'T'}) {
        for (int k = 1; k <= n; ++k) {
          z[k] = ch == s[k-1] ? y[k-1]+1 : max(y[k], z[k-1]);
        }
#ifdef DEBUG
        cout << "cur[" << bitset<N>(code(z)) << "]+=pre[" << bitset<N>(j) << "],"
            << cur[code(z)] << "+=" << pre[j] << ", ch=" << ch << '\n';
#endif
        cur[code(z)] += pre[j];
      }
    }
  }
  for (int i = 0; i < 1<<n; ++i) res[__builtin_popcount(i)] += dp[m&1][i];
  for (int i = 0; i <= n; ++i) cout << res[i] << '\n';
}


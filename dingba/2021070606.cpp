// 0|1 black|white
inline void solve() {
  cin >> n;
  for (int i = 1; i <= n; ++i) {
    static string str;
    cin >> str;
    a[i] = 0;
    for (int j = 0; j < n; ++j) {
      if (str[j] == '*') a[i] |= 1<<j;
    }
  }
  vector<int> vp, vc(n+1, 0);
  dp[0].clear();
  dp[0][vc] = 1;
  for (int i = 1; i <= n; ++i) {
    map<vector<int>, Mint> &cur = dp[i&1], &pre = dp[~i&1];
    cur.clear();
#ifdef DEBUG
    cout << i << "*************" << bitset<N>(a[i]) << '\n';
#endif
    for (auto it = pre.begin(); it != pre.end(); ++it) {
      vp = it->first;
#ifdef DEBUG
      print(vp);
#endif
      for (int j = 0; j < 1<<n; ++j) if ((a[i]&j) == 0) {
        vc[0] = j&1;
        vc[n] = max(vp[n], vc[0]);
        for (int k = 1; k < n; ++k) {
          if ((j>>k)&1) {
            vc[k] = min({vp[k], vc[k-1], vp[k-1]})+1;
            vc[n] = max(vc[n], vc[k]);
          } else {
            vc[k] = 0;
          }
        }
#ifdef DEBUG
        cout << "+" << bitset<N>(j) << "->";
        print(vc);
#endif
        cur[vc] += it->second;
      }
    }
  }
  memset(res, 0, sizeof(Mint)*(n+1));
  for (auto it : dp[n&1]) res[it.first[n]] += it.second;
  for (int i = 0; i <= n; ++i) cout << res[i] << '\n';
}


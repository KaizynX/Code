int n;
int c[N];
Mint dp2[2][N][N][3][3], dp1[N][N][2][2];

inline void solve() {
  cin >> n;
  for (int i = 0; i < 26; ++i) cin >> c[i];

  int w1[2] = {25, 1};
  for (int i2 = 0; i2 < 2; ++i2)
  for (int i3 = 0; i3 < 2; ++i3)
    dp1[2][i2+i3][i2][i3] = w1[i2]*w1[i3];
  for (int i = 3; i <= n; ++i)
  for (int i1 = 0; i1 < n; ++i1)
  for (int i2 = 0; i2 < 2; ++i2)
  for (int i3 = 0; i3 < 2; ++i3)
  for (int i4 = 0; i4 < 2; ++i4) {
    if (i1-i2 < 0) continue;
    dp1[i][i1][i2][i3] += dp1[i-1][i1-i2][i3][i4]*(w1[i2]-(i2==i4));
  }

  int w2[3] = {24, 1, 1};
  for (int i3 = 0; i3 < 3; ++i3)
  for (int i4 = 0; i4 < 3; ++i4)
    dp2[2&1][(i3==1)+(i4==1)][(i3==2)+(i4==2)][i3][i4] = w2[i3]*w2[i4];
  for (int i = 3; i <= n; ++i) {
    memset(dp2[i&1], 0, sizeof dp2[0]);
    for (int i1 = 0; i1 < n; ++i1)
    for (int i2 = 0; i2 < n; ++i2)
    for (int i3 = 0; i3 < 3; ++i3)
    for (int i4 = 0; i4 < 3; ++i4)
    for (int i5 = 0; i5 < 3; ++i5) {
      // from i4,i5 add i3 to i3 i4
      if (i1-(i3==1) < 0 || i2-(i3==2) < 0) continue;
      dp2[i&1][i1][i2][i3][i4] += dp2[~i&1][i1-(i3==1)][i2-(i3==2)][i4][i5]*(w2[i3]-(i3==i5));
    }
  }

  Mint res = 26*26;
  for (int i = 3; i <= n; ++i) res *= 25; // char[i] != char[i-2]
  for (int i = 0; i < 26; ++i)
  for (int j = c[i]+1; j <= n; ++j)
  for (int i2 = 0; i2 < 2; ++i2)
  for (int i3 = 0; i3 < 2; ++i3)
    res -= dp1[n][j][i2][i3];
  for (int i1 = 0; i1 < 26; ++i1)
  for (int i2 = 0; i2 < i1; ++i2)
  for (int j1 = c[i1]+1; j1 <= n; ++j1)
  for (int j2 = c[i2]+1; j2 <= n; ++j2)
  for (int i3 = 0; i3 < 3; ++i3)
  for (int i4 = 0; i4 < 3; ++i4)
    res += dp2[n&1][j1][j2][i3][i4];
  cout << res << '\n';
}


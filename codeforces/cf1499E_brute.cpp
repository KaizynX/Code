Mint f(string x, string y) {
  int n = x.size();
  int m = y.size();
  Mint res = 0;
  string z, xx, yy;
  // cout << x << " " << y << "\n";
  for (int i = 0; i < 1<<(n+m); ++i) {
    if (__builtin_popcount(i) == n) {
      xx = x, yy = y; z = "";
      for (int j = 0; j < n+m; ++j) {
        if ((i>>j)&1) {
          z += xx.back();
          xx.pop_back();
        } else {
          z += yy.back();
          yy.pop_back();
        }
      }
      res += check(z);
    }
  }
  return res;
}


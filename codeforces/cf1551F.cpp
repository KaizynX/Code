constexpr int md = 1e9+7;
using Mint = Modular<std::integral_constant<decay<decltype(md)>::type, md>>;

const int N = 1e2+7;

int n, k;
int cnt[N];
Mint dp[N];
vector<int> e[N], v[N];

void dfs(int u, int fa, int dep, int k) {
  cnt[dep] += k;
  for (int v : e[u]) if (v != fa) {
    dfs(v, u, dep+1, k);
  }
}

inline void solve() {
  cin >> n >> k;
  for (int i = 1; i <= n; ++i) e[i].clear();
  for (int i = 1, u, v; i < n; ++i) {
    cin >> u >> v;
    e[u].emplace_back(v);
    e[v].emplace_back(u);
  }
  if (k == 2) return cout << n*(n-1)/2 << '\n', void();
  Mint res = 0;
  for (int i = 1; i <= n; ++i) if ((int)e[i].size() >= k) {
#ifdef DEBUG
    cout << i << "'th is root **********\n";
#endif
    for (int j = 1; j <= n; ++j) v[j].clear();
    for (int j : e[i]) {
      dfs(j, i, 1, 1);
      for (int d = 1; d <= n; ++d) v[d].emplace_back(cnt[d]);
#ifdef DEBUG
      cout << j << ":";
      for (int d = 1; d <= n; ++d) cout << cnt[d] << " \n"[d==n];
#endif
      dfs(j, i, 1, -1);
    }
    for (int d = 1; d <= n; ++d) {
      for (int j = 1; j <= k; ++j) dp[j] = 0;
      dp[0] = 1;
      for (int t : v[d]) if (t) {
        for (int j = k; j; --j) {
          dp[j] += dp[j-1]*t;
        }
      }
#ifdef DEBUG
      cout << "d=" << d << ":";
      for (int t : v[d]) cout << t << ' ';
      cout << '\n';
      for (int j = 0; j <= k; ++j) cout << dp[j] << " \n"[j==k];
#endif
      res += dp[k];
    }
  }
  cout << res << '\n';
}


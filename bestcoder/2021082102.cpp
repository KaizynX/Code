struct Combination {
  int fac[N], inv[N];
  void init(const int &n) {
    fac[0] = inv[0] = fac[1] = inv[1] = 1;
    for (int i = 2; i <= n; ++i) {
      fac[i] = 1ll*fac[i-1]*i%MOD;
      inv[i] = 1ll*(MOD-MOD/i)*inv[MOD%i]%MOD;
    }
    for (int i = 2; i <= n; ++i) {
      inv[i] = 1ll*inv[i]*inv[i-1]%MOD;
    }
  }
  int A(const int &p, const int &q) {
    return p >= q ? 1ll*fac[p]*inv[p-q]%MOD : 0;
  }
  int C(const int &p, const int &q) {
    return p >= q ? 1ll*fac[p]*inv[q]%MOD*inv[p-q]%MOD : 0;
  }
};

int n, m;
Mint dp[N][N][2];
Mint S[N][N];
Combination comb;

inline void stirling(const int &n) {
  S[0][0] = 1;
  // 注意取模
  for (int i = 1; i <= n; ++i)
    for (int j = 1; j <= i; ++j)
      S[i][j] = S[i-1][j-1]+S[i-1][j]*j;
  for (int i = 1; i <= n; ++i)
    for (int j = 1; j <= i; ++j)
      S[i][j] *= comb.fac[j];
}

inline void brute() {
  // cin >> n >> m;
  dp[0][0][0] = dp[0][0][1] = 1;
  for (int i = 0; i <= n; ++i) {
    for (int j = 0; j <= m; ++j) {
      if (i == 0 && j == 0) continue;
      dp[i][j][0] = dp[i][j][1] = 0;
      for (int k = 0; k < i; ++k) dp[i][j][0] += dp[k][j][1]*comb.C(n-k, i-k);
      for (int k = 0; k < j; ++k) dp[i][j][1] += dp[i][k][0]*comb.C(m-k, j-k);
      #ifdef DEBUG
      // printf("dp[%d][%d]=%d\n", i, j, (int)dp[i][j]);
      #endif
    }
  }
  cout << dp[n][m][0]+dp[n][m][1] << '\n';
}

void f() {
  Mint ans = 0;
  for (int i = 1; i <= n; ++i) {
    for (int j = max(1, i-1); j <= min(i+1, m); ++j) {
      ans += S[n][i]*S[m][j];
    }
    if (i <= m) ans += S[n][i]*S[m][i];
    // ans += S[n][i]*(S[m][i-1]+2*S[m][i]+S[m][i+1]);
  }
  cout << ans << '\n';
}


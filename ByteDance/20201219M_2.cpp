template <typename T>
struct BinaryIndexedTree {
  int n;
  T tr[N*4];
  void init(const int &_n) { n = _n; clear(); }
  void clear() { for (int i = 0; i <= n; ++i) tr[i] = 0; }
  void add(const int &x, const T &v) { for (int i = x; i <= n; i += i&-i) tr[i] += v; }
  void add(const int &x, const int &y, const T &v) { add(x, v); add(y+1, -v); }
  T query(const int &x) { T res = 0; for (int i = x ; i; i -= i&-i) res += tr[i]; return res; }
  T query(const int &x, const int &y) { return query(y)-query(x-1); }
};

int n, m;
Mint fac[N];
vector<int> d;
pii a[N], b[N];
BinaryIndexedTree<Mint> sum, cnt;

void discrete() {
  for (int i = 1; i <= n; ++i) {
    d.emplace_back(a[i].first);
    d.emplace_back(a[i].second);
    d.emplace_back(b[i].first);
    d.emplace_back(b[i].second);
  }
  sort(d.begin(), d.end());
  d.erase(unique(d.begin(), d.end()), d.end());
  m = d.size();
  for (int i = 1; i <= n; ++i) {
    a[i].first = lower_bound(d.begin(), d.end(), a[i].first)-d.begin()+1;
    a[i].second = lower_bound(d.begin(), d.end(), a[i].second)-d.begin()+1;
    b[i].first = lower_bound(d.begin(), d.end(), b[i].first)-d.begin()+1;
    b[i].second = lower_bound(d.begin(), d.end(), b[i].second)-d.begin()+1;
  }
}

Mint calc(pii *arr) {
  cnt.init(m);
  sum.init(m);
  Mint res = 0, xx, yy;
  for (int i = 1, x, y; i <= n; ++i) {
    x = arr[i].first;
    y = arr[i].second;
    xx = d[x-1];
    yy = d[y-1];
    res += sum.query(x, y-1)+cnt.query(y, m)*yy-cnt.query(x, m)*xx;
    cnt.add(y, 1);
    sum.add(y, yy);
  }
  return res;
}


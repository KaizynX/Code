const int N = 3e5+7;

int n, w;
pii a[N];

inline void solve() {
  cin >> n >> w;
  for (int i = 1; i <= n; ++i) {
    cin >> a[i].first >> a[i].second;
  }
  sort(a+1, a+n+1);
  reverse(a+1, a+n+1);
  ll ans = 0;
  for (int i = 1; i <= n; ++i) {
    int k = min(w, a[i].second);
    w -= k;
    ans += 1ll*k*a[i].first;
  }
  cout << ans << '\n';
}

